qiqi_section() {
    local title="$1"
    printf "\n${QIQI_PINK}в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ %s в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${QIQI_PLAIN}\n" "$title"
}

qiqi_menu_item() {
    local num="$1"
    local label="$2"
    local desc="${3:-}"
    if [ -n "$desc" ]; then
        printf "  ${QIQI_GREEN}[ %s ]${QIQI_PLAIN}  %s ${QIQI_GRAY}%s${QIQI_PLAIN}\n" "$num" "$label" "$desc"
    else
        printf "  ${QIQI_GREEN}[ %s ]${QIQI_PLAIN}  %s\n" "$num" "$label"
    fi
}

